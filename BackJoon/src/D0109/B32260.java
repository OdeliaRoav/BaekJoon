package D0109;

import java.util.Scanner;

public class B32260 {
	
	static int sum(int a, int b) {
		int sum = a + b;
		
		
		return sum;
		
	}

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		
		int a = sc.nextInt();
		int b = sc.nextInt();
		
		System.out.print(sum(a,b));
		
		
		
	}

}
